package findPairs;

public class Pair {
    private int a;
    private int b;

    public Pair(int newA, int newB) {
        a = newA;
        b = newB;
    }

    public void setA(int newA) {
        a = newA;
    }

    public void setB(int newB) {
        b = newB;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }
}
